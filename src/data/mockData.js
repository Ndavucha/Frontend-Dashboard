export const generateDailySupply = () => {
  const startDate = new Date('2025-01-20');
  const days = [];
  
  for (let i = 0; i < 14; i++) {
    const date = new Date(startDate);
    date.setDate(date.getDate() + i);
    const dateStr = date.toISOString().split('T')[0];
    
    const demand = 30;
    const farmerSupply = Math.floor(Math.random() * 20) + 15;
    const aggregatorSupply = Math.max(0, demand - farmerSupply - Math.floor(Math.random() * 5));
    const allocatedSupply = farmerSupply + aggregatorSupply;
    
    days.push({
      date: dateStr,
      demand,
      allocatedSupply,
      balance: demand - allocatedSupply,
      allocatedSuppliers: [
        { name: 'Mary Wanjiku', quantity: Math.floor(farmerSupply * 0.4), type: 'farmer' },
        { name: 'John Kiprop', quantity: Math.floor(farmerSupply * 0.35), type: 'farmer' },
        { name: 'Peter Mutua', quantity: Math.floor(farmerSupply * 0.25), type: 'farmer' },
        ...(aggregatorSupply > 0 ? [{ name: 'KenAgri Aggregators', quantity: aggregatorSupply, type: 'internal-aggregator' }] : []),
      ],
    });
  }
  
  return days;
};

